/**
 * Generate src/components/games/chess/ChessPiece.tsx from the canonical
 * Cburnett chess piece SVGs (tri-licensed GFDL/BSD/GPL — used under the BSD
 * license; artwork (c) Colin M.L. Burnett). Recolors to the Gambit palette.
 *
 * Usage: node scripts/gen-chess-pieces.js /tmp/chess
 */
const fs = require("fs");
const path = require("path");

const SRC = process.argv[2] || "/tmp/chess";
const OUT = path.join(__dirname, "..", "src", "components", "games", "chess", "ChessPiece.tsx");

// brand palette
const W_BODY = "#f2ead8"; // ivory
const W_LINE = "#221e2e"; // dark outline on white pieces
const B_BODY = "#2e2a3c"; // charcoal-violet
const B_EDGE = "#0a0812"; // outline on dark pieces
const B_DETAIL = "#cfc9e2"; // light detail strokes on dark pieces

const KEBABS = [
  "fill-rule",
  "fill-opacity",
  "stroke-width",
  "stroke-linecap",
  "stroke-linejoin",
  "stroke-miterlimit",
  "stroke-opacity",
  "stroke-dasharray",
  "stroke-dashoffset",
  "xml:space",
];

function camel(k) {
  if (k === "xml:space") return "xmlSpace";
  return k.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
}

function recolor(prop, val, dark) {
  const v = val.trim().toLowerCase();
  const isWhiteHex = v === "#ffffff" || v === "#fff" || v === "white";
  const isBlackHex = v === "#000000" || v === "#000" || v === "black";
  if (!dark) {
    if (isWhiteHex) return W_BODY;
    if (isBlackHex) return W_LINE;
  } else {
    if (isWhiteHex) return B_DETAIL;
    if (isBlackHex) return prop === "fill" ? B_BODY : B_EDGE;
  }
  return val.trim();
}

function transform(svg, dark) {
  let inner = svg.match(/<svg[^>]*>([\s\S]*)<\/svg>/)[1];
  inner = inner.replace(/<!--[\s\S]*?-->/g, "");
  // expand style="a:b; c:d" into attributes (recolored)
  inner = inner.replace(/style="([^"]*)"/g, (_, body) => {
    const out = [];
    for (const pair of body.split(";")) {
      const i = pair.indexOf(":");
      if (i < 0) continue;
      const k = pair.slice(0, i).trim();
      let v = pair.slice(i + 1).trim();
      if (!k) continue;
      v = recolor(k, v, dark);
      out.push(`${camel(k)}="${v}"`);
    }
    return out.join(" ");
  });
  // any remaining bare kebab attributes → camelCase
  for (const k of KEBABS) {
    inner = inner.replace(new RegExp(`${k}=`, "g"), `${camel(k)}=`);
  }
  // recolor bare fill=/stroke= attributes too
  inner = inner.replace(/(fill|stroke)="([^"]*)"/g, (_, p, v) => `${p}="${recolor(p, v, dark)}"`);
  return inner.trim().replace(/\n\s*\n/g, "\n");
}

const TYPES = ["p", "r", "n", "b", "q", "k"];
const parts = [];
for (const t of TYPES) {
  for (const c of ["l", "d"]) {
    const raw = fs.readFileSync(path.join(SRC, `${t}${c}.svg`), "utf8");
    const jsx = transform(raw, c === "d")
      .split("\n")
      .map((l) => "      " + l.trim())
      .join("\n");
    parts.push(`  "${t}${c}": (\n    <>\n${jsx}\n    </>\n  ),`);
  }
}

const file = `"use client";

import { PieceSymbol, Color } from "chess.js";

/**
 * Canonical Staunton chess pieces — artwork by Colin M.L. Burnett ("Cburnett"),
 * tri-licensed GFDL / BSD / GPL; used here under the BSD license with this
 * attribution retained. Recolored to the Gambit palette (ivory / charcoal).
 * Generated by scripts/gen-chess-pieces.js — edit that script, not this file.
 * viewBox 0 0 45 45.
 */
export function ChessPiece({
  type,
  color,
  size = 44,
  className,
}: {
  type: PieceSymbol;
  color: Color;
  size?: number;
  className?: string;
}) {
  return (
    <svg
      viewBox="0 0 45 45"
      width={size}
      height={size}
      className={className}
      style={{ filter: "drop-shadow(0 2px 2px rgba(0,0,0,0.45))" }}
    >
      <ChessGlyph type={type} color={color} />
    </svg>
  );
}

/**
 * Just the piece markup (no <svg> wrapper) so it can be dropped into any SVG
 * scene — board art, the homepage demo, etc. — at an arbitrary transform.
 */
export function ChessGlyph({ type, color }: { type: PieceSymbol; color: Color }) {
  return <g>{GLYPHS[\`\${type}\${color === "w" ? "l" : "d"}\`]}</g>;
}

const GLYPHS: Record<string, JSX.Element> = {
${parts.join("\n")}
};
`;

fs.writeFileSync(OUT, file);
console.log("Wrote", OUT, fs.statSync(OUT).size, "bytes");
